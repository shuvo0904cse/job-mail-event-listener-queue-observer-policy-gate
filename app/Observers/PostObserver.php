<?php

namespace App\Observers;

use App\Jobs\SendNewPostToUsersJob;
use App\Models\Post;

class PostObserver
{

    public function creating(Post $post)
    {
        $post->name = "post_from_observer_".$post->name;
    }

    /**
     * Handle the Post "created" event.
     *
     * @param  \App\Models\Post  $post
     * @return void
     */
